public class FtoC {

    public static void main(String[] args) {
      int c1=289;
      int c2=400;
      int c3=-36;
      int c4=-180;
      float f1=c1*(9.0f/5.0f)+32.0f;
      float f2=c2*(9.0f/5.0f)+32.0f;
      float f3=c3*(9.0f/5.0f)+32.0f;
      float f4=c4*(9.0f/5.0f)+32.0f;
        System.out.println("Fahrenheit to celsius"); 
        System.out.println("289 Celsius = "+f1+"Fahrenheit");
        System.out.println("400 Celsius = "+f2+"Fahrenheit");
        System.out.println("-36 Celsius = "+f3+"Fahrenheit");
        System.out.println("-180 Celsius = "+f4+"Fahrenheit");
    }
}
